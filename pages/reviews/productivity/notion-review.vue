            <h2>Key Features</h2>
            <ul>
              <li><strong>Flexible Pages and Blocks</strong>: Create rich documents with nested pages and dozens of block types (text, images, tables, embeds, etc.)</li>
              <li><strong>Powerful Databases</strong>: Build custom databases with different views (table, kanban, calendar, list, gallery)</li>
              <li><strong>Templates</strong>: Hundreds of pre-built and community templates for various use cases</li>
              <li><strong>Collaboration</strong>: Real-time editing, comments, and sharing options</li>
              <li><strong>API and Integrations</strong>: Connect with other tools and build custom workflows</li>
              <li><strong>Cross-Platform</strong>: Available on web, desktop (Windows, Mac), and mobile (iOS, Android)</li>
            </ul>

            <h2>Pricing (2025)</h2>
            <ul>
              <li><strong>Free Plan</strong>: Unlimited pages and blocks for individuals, with some limitations on file uploads (5MB) and guests</li>
              <li><strong>Plus Plan</strong>: $10/month - Unlimited file uploads, unlimited guests, 30-day page history</li>
              <li><strong>Business Plan</strong>: $18/month per user - Advanced permissions, SAML SSO, private teamspaces</li>
              <li><strong>Enterprise Plan</strong>: Custom pricing - Dedicated support, advanced security, user provisioning</li>
            </ul>

            <h2>Pros</h2>
            <ul>
              <li>Extremely flexible and customizable to fit almost any workflow</li>
